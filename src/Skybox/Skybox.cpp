Skybox::Skybox()
{
    // Create the shader for the skybox
    skyboxShader = new Shader("Shaders/Skybox/Skybox.vert", "Shaders/Skybox/Skybox.frag");
    setupMesh();
}

void Skybox::load(const std::vector<std::string>& faces)
{
    glGenTextures(1, &cubemapTexture);
    glBindTexture(GL_TEXTURE_CUBE_MAP, cubemapTexture);

    int width, height, nrChannels;
    for (unsigned int i = 0; i < faces.size(); i++)
    {
        unsigned char* data = stbi_load(faces[i].c_str(), &width, &height, &nrChannels, 0);
        if (data)
        {
            // Note: GL_TEXTURE_CUBE_MAP_POSITIVE_X + i works because the enum values are sequential.
            glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, GL_RGBA, width, height, 0, GL_RGBA, GL_UNSIGNED_BYTE, data);
            stbi_image_free(data);
        }
        else
        {
            std::cout << "Cubemap texture failed to load at path: \n" << faces[i];
            stbi_image_free(data);
        }
    }

    // Set texture parameters
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);
}

void Skybox::setupMesh()
{
    glGenVertexArrays(1, &vao);
    glGenBuffers(1, &vbo);

    glBindVertexArray(vao);
    glBindBuffer(GL_ARRAY_BUFFER, vbo);
    glBufferData(GL_ARRAY_BUFFER, sizeof(skyboxVertices), &skyboxVertices, GL_STATIC_DRAW);

    // Position attribute
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);

    glBindVertexArray(0);
}

void Skybox::draw(const glm::mat4& view, const glm::mat4& projection)
{
    // Change the depth function to LEQUAL because the skybox is rendered at the maximum depth (1.0).
    // We want it to pass the depth test when the buffer is cleared to 1.0.
    glDepthFunc(GL_LEQUAL);

    skyboxShader->use();

    // Remove the translation component from the view matrix. This makes the skybox
    // centered on the camera, creating the illusion that it's infinitely far away.
    glm::mat4 viewWithoutTranslation = glm::mat4(glm::mat3(view));
    skyboxShader->setUniformValue("view", viewWithoutTranslation);
    skyboxShader->setUniformValue("projection", projection);
    skyboxShader->setUniformValue("skybox", 0);

    // Draw the skybox cube
    glBindVertexArray(vao);
    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_CUBE_MAP, cubemapTexture);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    glBindVertexArray(0);

    // Restore the default depth function
    glDepthFunc(GL_LESS);
}
